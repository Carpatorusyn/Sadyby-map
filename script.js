searchInput.addEventListener('input', function(e) {
    const query = e.target.value.toLowerCase();
    searchResults.innerHTML = '';
    
    if (query.length === 0) {
        searchResults.classList.add('hidden');
        return;
    }
    
    searchResults.classList.remove('hidden');
    const filtered = markers.filter(m => m.name.includes(query));
    
    if (filtered.length === 0) {
        const li = document.createElement('li');
        li.textContent = "Садиб не знайдено";
        li.style.color = "#999";
        searchResults.appendChild(li);
        return;
    }

    filtered.forEach(item => {
        const li = document.createElement('li');
        li.textContent = item.feature.properties.name;
        
        // Клік по результату пошуку
        li.addEventListener('click', () => {
            const isPanelAlreadyOpen = !!selectedMarker;

            if (!isPanelAlreadyOpen) {
                previousViewState = { center: map.getCenter(), zoom: map.getZoom() };
            }

            if (selectedMarker) {
                selectedMarker.setIcon(estateIcon);
            }
            item.marker.setIcon(selectedEstateIcon);
            selectedMarker = item.marker;

            if (window.innerWidth <= 1024) {
                map.flyTo(item.latlng, map.getZoom(), { duration: 0.5 });

                const popupContent = document.createElement('div');
                popupContent.className = 'custom-estate-popup';
                popupContent.innerHTML = `
                    <h4>${item.feature.properties.name}</h4>
                    <span>Детальніше...</span>
                `;

                popupContent.addEventListener('click', () => {
                    map.closePopup();
                    showEstateInfo(item.feature.properties, item.feature.geometry.coordinates);
                    flyToMarker(item.latlng, isPanelAlreadyOpen);
                });

                map.closePopup();

                L.popup({ 
                    closeButton: true, 
                    closeOnClick: false,
                    offset: [0, -32], 
                    autoPan: false 
                })
                .setLatLng(item.latlng)
                .setContent(popupContent)
                .openOn(map);

            } else {
                map.closePopup(); // Гарантуємо відсутність віконця на ПК
                showEstateInfo(item.feature.properties, item.feature.geometry.coordinates);
                flyToMarker(item.latlng, isPanelAlreadyOpen);
            }

            searchResults.classList.add('hidden');
            searchInput.value = item.feature.properties.name;

            searchResults.classList.add('hidden');
            searchInput.value = item.feature.properties.name;
        });
        searchResults.appendChild(li);
    });
});

