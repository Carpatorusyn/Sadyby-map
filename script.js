L.geoJSON(estatesGeoJSON, {
    pointToLayer: function (feature, latlng) {
        // Використовуємо НОВУ іконку
        const marker = L.marker(latlng, { icon: estateIcon });
        
        // Клік по маркеру садиби на карті
        marker.on('click', () => {
            const isPanelAlreadyOpen = !!selectedMarker;

            // Якщо панель була закрита, запам'ятовуємо позицію екрана
            if (!isPanelAlreadyOpen) {
                previousViewState = { center: map.getCenter(), zoom: map.getZoom() };
            }

            // Повертаємо попередньому маркеру звичайну іконку
            if (selectedMarker) {
                selectedMarker.setIcon(estateIcon);
            }
            // Задаємо новому (клікнутому) маркеру активну іконку
            marker.setIcon(selectedEstateIcon);
            selectedMarker = marker;

            // Стандартна логіка: на мобільному відкриваємо попап, на ПК - бічну панель
            if (window.innerWidth <= 1024) {
                map.flyTo(latlng, map.getZoom(), { duration: 0.5 });

                const popupContent = document.createElement('div');
                popupContent.className = 'custom-estate-popup';
                popupContent.innerHTML = `
                    <h4>${feature.properties.name}</h4>
                    <span>Детальніше...</span>
                `;

                popupContent.addEventListener('click', () => {
                    map.closePopup(); // Закриваємо віконце
                    showEstateInfo(feature.properties, feature.geometry.coordinates);
                    flyToMarker(latlng, isPanelAlreadyOpen);
                });

                map.closePopup(); // Прибираємо попереднє віконце перед відкриттям нового

                // Створюємо незалежне віконце
                L.popup({ 
                    closeButton: true, 
                    closeOnClick: false,
                    offset: [0, -20], // Змінив відступ під нові круглі іконки
                    autoPan: false    // МАГІЯ: забороняємо ламати анімацію камери
                })
                .setLatLng(latlng)
                .setContent(popupContent)
                .openOn(map);

            } else {
                map.closePopup(); // Залізно гарантуємо, що віконця не буде на ПК
                showEstateInfo(feature.properties, feature.geometry.coordinates);
                flyToMarker(latlng, isPanelAlreadyOpen);
            }
        });
        
        // Додаємо дані в масив для живого пошуку
        markers.push({
            name: feature.properties.name.toLowerCase(),
            feature: feature,
            latlng: latlng,
            marker: marker
        });
        
        return marker;
    }
}).addTo(map);

