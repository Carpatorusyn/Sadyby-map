// 1. Ініціалізація карти (Обмеження області та мінімального зуму)
const southWest = L.latLng(44.0, 22.0); // Приблизно кордон Румунії/Західної України
const northEast = L.latLng(55.0, 42.0); // Приблизно кордон Білорусі/Росії
const bounds = L.latLngBounds(southWest, northEast);

const map = L.map('map', {
    zoomControl: false, // Вимикаємо стандартні кнопки зуму
    maxBounds: bounds, // Обмежуємо переміщення
    maxBoundsViscosity: 1.0, // Карта не буде "пружинити" за межі
    minZoom: 6 // Забороняємо віддаляти на рівень всієї планети
}).setView([51.0, 31.0], 8);

map.on('zoom', function() {
    // Нічого не робимо, це просто примушує карту перерахувати 
    // z-index та позиції маркерів під час зуму
});



// Додаємо кнопки зуму в правий нижній кут
L.control.zoom({ position: 'bottomright' }).addTo(map);

// 2. Базові шари
const osm = L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
    maxZoom: 19,
    attribution: '© OpenStreetMap'
});

const sadybyLabelsLayer = L.layerGroup();

const satellite = L.tileLayer('https://server.arcgisonline.com/ArcGIS/rest/services/World_Imagery/MapServer/tile/{z}/{y}/{x}', {
    attribution: 'Tiles © Esri'
});

// Додаємо звичайну карту за замовчуванням
osm.addTo(map);

// 3. Історичний шар (Карта 2204328)
const imageUrl = '2204328.png'; // Не забудь змінити на .webp, якщо стискав файл
const imageBounds = [
    [52.865262619479, 30.129386222222], // Північний Захід
    [49.412328710214, 35.622215111111]  // Південний Схід
];

const historicalLayer = L.imageOverlay(imageUrl, imageBounds, {
    opacity: 1,
    interactive: false
}).addTo(map);

// 4. Перемикач шарів (Control.Layers)
const baseMaps = {
    "Звичайна карта": osm,
    "Супутник": satellite
};
const overlays = {
    "Історична карта": historicalLayer,
    "Показувати назви": sadybyLabelsLayer // Додаємо тумблер у меню шарів
};
L.control.layers(baseMaps, overlays, { position: 'topright' }).addTo(map);

// 5. Керування прозорістю історичного шару
const opacitySlider = document.getElementById('opacity-slider');
opacitySlider.addEventListener('input', function (e) {
    historicalLayer.setOpacity(e.target.value);
});


// ==========================================
// Логіка масового відображення назв (Легкі Tooltips)
// ==========================================
map.on('overlayadd', function(e) {
    if (e.name === "Показувати назви") {
        showNamesActive = true; // Шар увімкнено
        
        markers.forEach(item => {
            item.marker.bindTooltip(item.feature.properties.name, {
                permanent: true, 
                direction: 'top', 
                className: 'custom-estate-tooltip' 
            });
            
            // Магія: показуємо назву ТІЛЬКИ якщо інтерактивне віконце цієї садиби зараз НЕ відкрите
            if (!item.marker.isPopupOpen()) {
                item.marker.openTooltip();
            }
        });
    }
});

map.on('overlayremove', function(e) {
    if (e.name === "Показувати назви") {
        showNamesActive = false; // Шар вимкнено
        markers.forEach(item => {
            item.marker.unbindTooltip(); 
        });
    }
});

map.on('overlayremove', function(e) {
    if (e.name === "Показувати назви") {
        permanentPopupsActive = false;
        
        markers.forEach(item => {
            item.marker.closePopup(); // Закриваємо попап
            item.marker.unbindPopup(); // Повністю відв'язуємо його, повертаючи карту до початкового стану
        });
    }
});

map.on('overlayremove', function(e) {
    // Коли юзер вимикає шар
    if (e.name === "Показувати назви") {
        markers.forEach(item => {
            item.marker.unbindTooltip(); // Прибираємо всі назви
        });
    }
});

// Автоматично ховаємо постійну назву, коли відкривається інтерактивне віконце
map.on('popupopen', function() {
    if (selectedMarker && showNamesActive) {
        selectedMarker.closeTooltip();
    }
});

// Автоматично повертаємо назву, коли інтерактивне віконце закривається
map.on('popupclose', function() {
    if (selectedMarker && showNamesActive) {
        selectedMarker.openTooltip();
    }
});

// Автоматичне закриття попапів при кліку на будь-яке порожнє місце карти
map.on('click', function(e) {
    // Перевіряємо, чи клікнули саме по карті, а не по маркеру чи панелі
    if (e.originalEvent.target.id === 'map' || e.originalEvent.target.classList.contains('leaflet-container')) {
        map.closePopup(); // Закриває відкрите віконце Leaflet
    }
});

